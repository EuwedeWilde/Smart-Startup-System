<section>
  <h3 class="docs__subtitle" id="manufacturing">Manufacturing</h3>
  <p class="docs__content">
    For manufacturing I used the following casing as a basis, but to make it useful some modifications needed to be made. The modifications in the cover lid a CNC can be used. It drills multiple holes in the cover, two big ones for the outlets, two smaller ones for the LED buttons, and eight small ones for mounting the outlet to the cover. 
  </p>
  <p class="docs__content">
    For the CNC I used the following settings:
  </p>
  <ul class="docs__content-list">
    <li><strong>4 mm single flute endmill</strong></li>
    <li>Max cut depth: <strong>4 mm</strong></li>
    <li>Depth per pass: <strong>0.3 to 0.6 mm</strong></li>
    <li>Plunge Rate: <strong>750</strong> *</li>
    <li>Feed Rate: <strong>2500</strong> *</li>
  </ul>
  <p class="docs__content-footnote">* Not 100% sure about these values. </p>
  <p class="docs__content">
    The casing it self was more tricky to modify. Due to its shape and ears, it could not be CNCed or laser cut, so we opted to do this by hand. I printed the shape of the C14 cutout and taped it with clear tape on one of the ends and then used a dremmel to cut it out. On the other side, I used a step drill to step 24 and a 3 mm drill to cut the necessary holes for the Ethernet port.
  </p>
